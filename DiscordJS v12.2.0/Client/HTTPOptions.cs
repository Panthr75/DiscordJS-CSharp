namespace DiscordJS
{
    /// <summary>
    /// HTTP options
    /// </summary>
    public class HTTPOptions
    {
        /// <summary>
        /// API version to use
        /// </summary>
        public int version;

        /// <summary>
        /// Base url of the API
        /// </summary>
        public string api;

        /// <summary>
        /// Base url of the CDN
        /// </summary>
        public string cdn;

        /// <summary>
        /// Base url of invites
        /// </summary>
        public string invite;
    }
}